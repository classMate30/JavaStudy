// 1부터 100까지의 정수 중에서
// 짝수들의 합을 계산하여 결과값을 출력하는 프로그램을 구현한다
// 단, 조건문을 사용하지 않도록 하며,
// while 반복문을 활용하여 작성할 수 있도록 한다.

//실행 예)
//1부터 100까지의 수 중 짝수의 합 : xxxx
// 계속하려면 아무 키나 누르세요...


//※문제 인식 및 분석
//	2 + 4 + 6 + 8 + 10 + ... + 100
//	- 초기값 : 2
//	- 증가값 : 2
//	- 한계값 : 100

public class Test045
{
	public static void main(String[] args)
	{
		int n=2, sum=0;
		while (n<=100)
		{
			sum += n;
			n += 2;
		}
		System.out.println("1부터 100까지의 수 중 짝수의 합 : " + sum);
	}
}

//실행 결과
